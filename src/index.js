const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let arr = [];
    let res = expr.split('**********');
    
    for (let i = 0; i < res.length; i++) {
      let str = '';
      arr[i] = str;
  
      for (let j = 0; j <= res[i].length; j += 2) {
        if (j % 10 === 0 && j !== 0 ) {
          arr[i] += MORSE_TABLE[str];
          str = '';
        }
        if (res[i][j] + res[i][j + 1] === '10') {
          str += '.';
        } else if (res[i][j] + res[i][j + 1] === '11') {
          str += '-';
        }
      }
      
    }
  
    return arr.join(' ');

}

module.exports = {
    decode
}